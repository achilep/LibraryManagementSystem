    private void lb_id_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lb_id_searchKeyReleased
        try {
            String sql="select * from librarian_Details where Id="+lb_id_search.getText()+" and Role='librarian'";
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                int id=rs.getInt("Id");
                lb_id_src.setText(Integer.toString(id));
                lb_name_src.setText(rs.getString("UserName"));
                lb_email_src.setText(rs.getString("Email"));
                lb_gender_src.setText(rs.getString("Gender"));
                lb_contact_src.setText(rs.getInt("Contact")+"");
            }else{
                lb_id_src.setText(null);
                lb_name_src.setText(null);
                lb_email_src.setText(null);
                lb_gender_src.setText(null);
                lb_contact_src.setText(null);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_lb_id_searchKeyReleased

