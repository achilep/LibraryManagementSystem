    private void lb_id1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_lb_id1KeyReleased
        String gender=null;
        try {
            String sql="select * from librarian_details where id="+lb_id1.getText()+" and role='librarian'";
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                lb_name1.setText(rs.getString("UserName"));
                lb_contact1.setText(rs.getInt("Contact")+"");
                lb_pass1.setText(rs.getString("Password"));
                lb_email1.setText(rs.getString("Email"));
                gender=rs.getString("Gender");
                if(gender.equalsIgnoreCase("male")){
                    jRadioButton3.setSelected(true);
                }else if(gender.equalsIgnoreCase("female")){
                    jRadioButton4.setSelected(true);
                }
            }else{
               lb_name1.setText(null);
                lb_contact1.setText(null);
                lb_pass1.setText(null);
                lb_email1.setText(null);
                buttonGroup1.clearSelection();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_lb_id1KeyReleased

